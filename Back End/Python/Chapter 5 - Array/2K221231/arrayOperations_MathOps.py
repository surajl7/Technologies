# Array:

# Array Operations:
# Mathematical Operations --> sin
# Ex. Print sin value

from numpy import *

arr1 = array([1, 2, 3, 4, 5])

print("\nsin values of array: ", sin(arr1), end='\n')
print("\ncos values of array: ", cos(arr1), end='\n')
print("\nlog values of array: ", log(arr1), end='\n')
print("\nsqrt value of array: ", sqrt(arr1), end='\n')
print("\nsum of elements of array: ", sum(arr1), end='\n')
print("\nmin of values of array: ", min(arr1), end='\n')
print("\nmax of elements of array: ", max(arr1), end='\n')
