# Exception Handling
#
# keywords:
#     1.  try --> test a block of code for errors
#     2.  except --> handle the error
#     3.  Finally --> execute the code, regardless of the result of the try and except blocks.

# Example1:
# print(x)    --> will show error as x is not defined

# Example2:
try:
    print(x)
except:
    print("An exception occurred")


