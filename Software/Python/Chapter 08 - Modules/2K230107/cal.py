# Calculus module used in the demo.py for it's functions

from math import *


# Function to add two numbers
def add(num1, num2):
    return num1 + num2


# Function to subtract two numbers
def sub(num1, num2):
    return num1 + num2


# Function to multiply two numbers
def mul(num1, num2):
    return num1 * num2


# Function to divide one number by another
def div(num1, num2):
    return num1 / num2


# Function to find a square root of number
def sqroot(num1):
    return sqrt(num1)
