/*
Ex. Addition of two numbers
*/

#include<iostream>
using namespace std;

int main()
{
    int num1, num2, sum;

    cout << "\nEnter 1st number: ";
    cin >> num1;
    cout << "\nEnter 2nd number: ";
    cin >> num2;

    sum = num1 + num2;

    cout << "\nAddition of " << num1 << " and " << num2 << " is " << sum <<"\n";

    return 0;
}
